// вводимо числа
var n = prompt('Введіть число:');

// перевірка на число
if (isNaN(n)) {
  console.error('Введене значення не є числом.');
} else {
  // перевірка на парність
  n = parseInt(n);
  if (n % 2 === 0) {
    console.log(n + ' є парним числом.');
  } else {
    console.log(n + ' є непарним числом.');
  }
}
